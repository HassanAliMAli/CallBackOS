import type { Lead, TranscriptMessage } from "../types"

function createTranscript(
  messages: Array<{ speaker: "Agent" | "Caller"; message: string }>,
  baseTime: Date
): TranscriptMessage[] {
  let offset = 0
  return messages.map((msg, index) => {
    offset += Math.floor(Math.random() * 8 + 3) * 1000
    return {
      id: `msg-${index}`,
      speaker: msg.speaker,
      message: msg.message,
      timestamp: new Date(baseTime.getTime() + offset),
    }
  })
}

